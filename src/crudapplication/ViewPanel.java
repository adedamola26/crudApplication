    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) empTable.getModel();
        model.setRowCount(0);
        for (User u : allUsers.getAllUsers()) {
            Object[] rows = new Object[8];
            rows[0] = u;
            rows[1] = u.getId();
            rows[2] = u.getAge();
            rows[3] = u.getGender();
            rows[4] = u.getDate();
            rows[5] = u.getLevel();
            rows[6] = u.getCellNum();
            rows[7] = u.getEmail();
            model.addRow(rows);
            clearFields();
        }
    }

